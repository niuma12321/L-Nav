type CategoryKey = keyof typeof EMOJI_CATEGORIES;

const STORAGE_KEY = 'ynav_recent_emojis';
const MAX_RECENT = 16;

// 获取最近使用的 emoji
const getRecentEmojis = (): string[] => {
  try {
    return JSON.parse(localStorage.getItem(STORAGE_KEY) || '[]');
  } catch {
    return [];
  }
};

// 添加 emoji 到最近使用
const addRecentEmoji = (emoji: string) => {
  const list = getRecentEmojis().filter(e => e !== emoji);
  const newList = [emoji, ...list].slice(0, MAX_RECENT);
  localStorage.setItem(STORAGE_KEY, JSON.stringify(newList));
};

const EmojiPicker: React.FC<EmojiPickerProps> = ({ onSelect, onClose, selectedEmoji }) => {
  const [searchQuery, setSearchQuery] = useState('');
  const [activeCategory, setActiveCategory] = useState<CategoryKey>('常用');
  const [recentEmojis, setRecentEmojis] = useState<string[]>([]);

  useEffect(() => {
    setRecentEmojis(getRecentEmojis());
  }, []);

  // 过滤 emoji
  const filteredEmojis = useMemo(() => {
    if (!searchQuery.trim()) return null;
    
    const allEmojis = Object.values(EMOJI_CATEGORIES).flatMap(cat => cat.emojis);
    const uniqueEmojis = [...new Set(allEmojis)];
    
    return uniqueEmojis.filter(emoji => 
      emoji.includes(searchQuery.trim())
    ).slice(0, 50);
  }, [searchQuery]);

  const currentEmojis = useMemo(() => {
    if (searchQuery.trim()) return filteredEmojis || [];
    if (activeCategory === '最近使用') return recentEmojis;
    return EMOJI_CATEGORIES[activeCategory].emojis;
  }, [searchQuery, activeCategory, recentEmojis, filteredEmojis]);

  const handleSelect = useCallback((emoji: string) => {
    addRecentEmoji(emoji);
    onSelect(emoji);
    onClose?.();
  }, [onSelect, onClose]);

  return (
    <div className="flex flex-col h-full max-h-[400px] bg-[#181a1c] rounded-xl overflow-hidden border border-white/10">
      {/* 搜索栏 */}
      <div className="p-3 border-b border-white/5">
        <div className="relative">
          <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-slate-500" />
          <input
            type="text"
            value={searchQuery}
            onChange={(e) => setSearchQuery(e.target.value)}
            placeholder="搜索 emoji..."
            className="w-full pl-9 pr-3 py-2 text-sm bg-[#0d0e10] rounded-lg border border-white/10 focus:border-orange-500/50 focus:outline-none text-white placeholder:text-slate-500"
            autoFocus
          />
          {searchQuery && (
            <button
              onClick={() => setSearchQuery('')}
              className="absolute right-2 top-1/2 -translate-y-1/2 p-1 rounded hover:bg-white/5"
            >
              <X className="w-3 h-3 text-slate-500" />
            </button>
          )}
        </div>
      </div>

      {/* 分类标签 */}
      <div className="px-2 py-2 border-b border-white/5 overflow-x-auto scrollbar-hide">
        <div className="flex gap-1">
          {(Object.keys(EMOJI_CATEGORIES) as CategoryKey[]).map((key) => {
            const CategoryIcon = EMOJI_CATEGORIES[key].icon;
            return (
              <button
                key={key}
                onClick={() => setActiveCategory(key)}
                disabled={!!searchQuery.trim()}
                className={`flex items-center gap-1 px-2.5 py-1.5 rounded-lg text-xs font-medium transition-all whitespace-nowrap ${
                  activeCategory === key && !searchQuery.trim()
                    ? 'bg-orange-500/20 text-orange-400'
                    : 'text-slate-400 hover:bg-white/5 hover:text-slate-300'
                } ${searchQuery.trim() ? 'opacity-50 cursor-not-allowed' : ''}`}
              >
                <CategoryIcon className="w-3.5 h-3.5" />
                <span className="hidden sm:inline">{key}</span>
              </button>
            );
          })}
        </div>
      </div>

      {/* 当前选择显示 */}
      {selectedEmoji && !searchQuery && (
        <div className="px-3 py-2 bg-[#0d0e10]/50 border-b border-white/5">
          <div className="flex items-center gap-2">
            <span className="text-xs text-slate-500">当前选择:</span>
            <span className="text-2xl">{selectedEmoji}</span>
          </div>
        </div>
      )}

      {/* Emoji 网格 */}
      <div className="flex-1 overflow-y-auto p-3">
        {currentEmojis.length === 0 ? (
          <div className="flex flex-col items-center justify-center py-8 text-slate-500">
            <Search className="w-8 h-8 mb-2 opacity-50" />
            <p className="text-sm">没有找到匹配的 emoji</p>
          </div>
        ) : (
          <div className="grid grid-cols-8 gap-1">
            {currentEmojis.map((emoji, index) => (
              <button
                key={`${emoji}-${index}`}
                onClick={() => handleSelect(emoji)}
                className={`aspect-square flex items-center justify-center text-xl rounded-lg transition-all hover:scale-110 hover:bg-white/5 ${
                  selectedEmoji === emoji ? 'bg-orange-500/20 ring-1 ring-orange-500/50' : ''
                }`}
                title={emoji}
              >
                {emoji}
              </button>
            ))}
          </div>
        )}
      </div>
    </div>
  );
};

export default EmojiPicker;
